familia         = ["Madalena","Gildenei","Gustavo","Mayara","Guilherme"]
idade_familia   = ["45","41","24","14","21"]
print(idade_familia)

# ordena em ordem crescente em caso de inteiros
idade_familia.sort()
print(idade_familia)

# ordena em ordem alfabetica em caso de strings
familia.sort()
print(familia)

# ordena em ordem decrescente/reverte em caso de inteiros
idade_familia.reverse()
print(idade_familia)

# reverte em ordem alfabetica em caso de strings
familia.reverse()
print(familia)
