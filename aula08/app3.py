# operadores de COMPARACAO

# ==            --> igual
# !=            --> diferente
# >             --> maior que
# <             --> menor que 
# >=            --> maior ou igual que
# <=            --> menor ou igual que 



# funciona com todos os tipos primitivos de dados

num1 = 50
num2 = 32

if num1 == num2:
    print("num1 um eh igual ao num2 dois")
elif num1 != num2:
    print("num1 um eh diferente do num2 dois") 


if num1 > num2:
    print("num1 e eh maior que num2")
elif num1 < num2:
    print("num1 e eh menor que num2")


if num1 >= num2:
    print("num1 e eh maior ou igual que o num2")
elif num1 <= num2:
    print("num1 e eh menor ou igual que o num2")
